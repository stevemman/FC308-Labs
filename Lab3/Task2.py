# Ask the user for a mathematical operator.
mathOp = input("Please enter a mathematical operator: ")

# Print the result of the comparison between mathOp and the mathematical operators.
print("mathOp == + gives the answer: ", mathOp == "+")
print("mathOp == - gives the answer: ", mathOp == "-")
print("mathOp == / gives the answer: ", mathOp == "/")
print("mathOp == * gives the answer: ", mathOp == "*")
