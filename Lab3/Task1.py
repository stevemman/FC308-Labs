# Ask the user for a value and cast into Integer.
value = int(input("Please enter a number : "))

# Print comparisons to 10 using all the relational operators.
print("value > 10 gives the answer: ", value > 10)
print("value < 10 gives the answer: ", value < 10)
print("value == 10 gives the answer: ", value == 10)
print("value >= 10 gives the answer: ", value >= 10)
print("value <= 10 gives the answer: ", value <= 10)
print("value != 10 gives the answer: ", value != 10)
