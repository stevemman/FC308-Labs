# Ask the user to enter a country.
country = input("Please enter a country: ")

if country == "Greece":
    print("The capital of Greece is Athens.")
elif country == "France":
    print("The capital of France is Paris.")
elif country == "Austria":
    print("The capital of Austria is Vienna.")
elif country == "China":
    print("The capital of China is Beijing.")
else:
    print("This country is not supported yet.")